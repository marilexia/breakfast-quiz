def recipe_generator(user_value)
     if user_value <14
        "breakfast_gluten" 
     elsif user_value <16
        "breakfast_v_v"
     elsif user_value <20
        "breakfast_none"
     elsif user_value <34
        "lunch_gluten"
        elsif user_value <36
        "lunch_v_v"
        elsif user_value<40
        "lunch_none"
        elsif user_value<54
        "dinner_gluten"
        elsif user_value<56
        "dinner_v_v"
        elsif user_value<60
        "dinner_none"
end
    
    
    # cranberry trailmix and chocolatey will be called later on in the app controller as pathways to respective cookie pages!
end
